/**
 * Now in Android navigation rail with header and content slots. Wraps Material 3 [NavigationRail].
 *
 * @param modifier Modifier to be applied to the navigation rail.
 * @param header Optional header that may hold a floating action button or a logo.
 * @param content Destinations inside the navigation rail. This should contain multiple
 * [NavigationRailItem]s.
 */
@Composable
fun AppNavigationRail(
    modifier: Modifier = Modifier,
    header: @Composable (ColumnScope.() -> Unit)? = null,
    content: @Composable ColumnScope.() -> Unit,
) {
    NavigationRail(
        modifier = modifier,
        containerColor = Color.Transparent,
        contentColor = AppNavigationDefaults.navigationContentColor(),
        header = header,
        content = content,
    )
}

@Preview
@Composable
private fun AppNavigationBarPreview() {
    val items = listOf("Home", "Payments", "Finance", "Profile")
    val icons =
        listOf(
            AppIcons.Home,
            AppIcons.Payment,
            AppIcons.Finance,
            AppIcons.Profile,
        )
    val selectedIcons =
        listOf(
            AppIcons.HomeBoarder,
            AppIcons.Payment,
            AppIcons.Finance,
            AppIcons.ProfileBoarder,
        )

    AppTheme {
        AppNavigationBar {
            items.forEachIndexed { index, item ->
                AppNavigationBarItem(
                    selected = index == 0,
                    onClick = { },
                    icon = {
                        Icon(
                            imageVector = icons[index],
                            contentDescription = item,
                        )
                    },
                    selectedIcon = {
                        Icon(
                            imageVector = selectedIcons[index],
                            contentDescription = item,
                        )
                    },
                    label = { Text(item) },
                )
            }
        }
    }
}

